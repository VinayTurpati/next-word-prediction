import re

def clean_text(text):
    text = text.lower()
    text = re.sub(r'[\?\.\!]+(?=[\?\.\!])', '', text)
    text = re.sub(r"i'm", "i am", text)
    text = re.sub(r"\'s", " is", text)
    text = re.sub(r"\'ll", " will", text)
    text = re.sub(r"\'ve", " have", text)
    text = re.sub(r"\'re", " are", text)
    text = re.sub(r"\'d", " would", text)
    text = re.sub(r"won't", "will not", text)
    text = re.sub(r"can't", "cannot", text)
    text = re.sub(r"\n't", " not", text)
    text = re.sub(r"n'", "ng", text)
    text = re.sub(r"'bout", "about", text)
    text = re.sub(r"'til", "until", text)
    text = re.sub(r"didngt", "did not", text)
    text = re.sub(r"they've", "they have", text)
    text = re.sub(r"how'd", "how did", text)
    text = re.sub(r"dongt", "do not", text)
    text = re.sub(r"shan't", "shall not", text)
    text = re.sub(r"wasngt", "was not", text)
    text = re.sub(r"couldngt", "could not", text)
    text = re.sub(r"isngt", "is not", text)
    text = re.sub(r"\'know", " know", text)
    text = re.sub(r"'em","them", text)
    text = re.sub(r"c'mon","come on", text)
    text = re.sub(r"c'mere","come here", text)
    text = re.sub(r"'", "", text)
    text = re.sub('[ \t\n\?\!\,]+', ' ',text)
    text = re.sub(r"[-()\"#/@;:<>{}`+=~|]", "", text)
    text = re.sub('(?<! )(?=[.,!?()])|(?<=[.,!?()])(?! )', r' ', text)
    return text

